"""
Multiply by operation.
"""


def times_by_one(a):
    """Return a * 1.
    """
    return a * 1


def times_by_two(a):
    """Return a * 2.
    """
    return a * 2


def times_by_three(a):
    """Return a * 3.
    """
    return a * 3


def times_by_four(a):
    """Return a * 4.
    """
    return a * 4
